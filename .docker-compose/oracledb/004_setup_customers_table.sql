CREATE TABLE address (
  address_pk                NUMBER       NOT NULL,
  address_type              VARCHAR2(20) DEFAULT 'POSTAL' NOT NULL,
  non_postal_address_ind    CHAR(1)      DEFAULT 'N' NOT NULL,
  non_paf_file_address_ind  CHAR(1)      DEFAULT 'N' NOT NULL,
  udprn                     CHAR(8),
  address_from_date         DATE         DEFAULT DATE '2000-01-01' NOT NULL,
  address_to_date           DATE,
  CONSTRAINT pk_address PRIMARY KEY (address_pk),
  CONSTRAINT ck_addr_postal_address_ind CHECK (non_postal_address_ind IN ('Y', 'N')),
  CONSTRAINT ck_addr_paf_file_address_ind CHECK (non_paf_file_address_ind IN ('Y', 'N'))
);

CREATE TABLE telecom_address (
  telecom_address_pk        NUMBER        NOT NULL,
  telecom_address_type      VARCHAR2(20)  NOT NULL,
  telecom_address_from_date DATE          DEFAULT DATE '2000-01-01' NOT NULL,
  telecom_address_to_date   DATE,
  internet_email_address    VARCHAR2(255),
  telephone_country_code    VARCHAR2(3),
  fax_number                VARCHAR2(35),
  extension_number          VARCHAR2(10),
  telephone_number          VARCHAR2(35),
  mobile_number             VARCHAR2(35),
  CONSTRAINT pk_telecom_address PRIMARY KEY (telecom_address_pk)
);

-- PARTY_VERSION_*_DATETIME kept as DATE (real: TIMESTAMP(3)) — documented divergence
-- (plan Risks): find-customers/get-customer-types compare PARTY_VERSION_TO_DATETIME
-- to the literal '31-DEC-9999' via NLS_DATE_FORMAT; a TIMESTAMP(3) column risks
-- ORA-01830/01858 under the DB-default NLS the app relies on. Equality-only, never
-- returned, so DATE is behaviourally identical for the fixture.
CREATE TABLE party_version (
  party_pk                     NUMBER       NOT NULL,
  party_version_pk             NUMBER       GENERATED BY DEFAULT AS IDENTITY,
  party_id                     VARCHAR2(20),
  party_type                   VARCHAR2(20) NOT NULL,
  preferred_language_code      VARCHAR2(20),
  party_ah_memo                VARCHAR2(4000),
  party_migrated_datetime      TIMESTAMP(3),
  party_mig_detail_check_ind   CHAR(1),
  party_data_last_check_dttm   TIMESTAMP(3),
  party_version_from_datetime  DATE         DEFAULT DATE '2000-01-01' NOT NULL,
  party_version_to_datetime    DATE         NOT NULL,
  party_data_check_deferred_dt DATE,
  password                     VARCHAR2(35),
  password_hint                VARCHAR2(20),
  authentication_refused_ind   CHAR(1),
  secondary_contact_full_name  VARCHAR2(70),
  CONSTRAINT pk_party_version PRIMARY KEY (party_version_pk),
  CONSTRAINT ck_pver_mig_detail_check_ind CHECK (party_mig_detail_check_ind IN ('Y', 'N', '?'))
);

CREATE TABLE person (
  party_pk            NUMBER       NOT NULL,
  person_title        VARCHAR2(35),
  person_given_name   VARCHAR2(35),
  person_given_name2  VARCHAR2(35),
  person_initials     VARCHAR2(35),
  person_family_name  VARCHAR2(35) NOT NULL,
  date_of_birth       DATE,
  memorable_date      DATE,
  memorable_date_hint VARCHAR2(255),
  date_of_death       DATE,
  digital_signature   BLOB,
  CONSTRAINT pk_person PRIMARY KEY (party_pk)
);
CREATE INDEX ix_pers_func_family_name ON person (person_family_name, party_pk);
CREATE INDEX ix_pers_func_given_name ON person (person_given_name, party_pk);

CREATE TABLE organisation (
  party_pk                   NUMBER        NOT NULL,
  organisation_name          VARCHAR2(255) NOT NULL,
  organisation_type          VARCHAR2(20)  DEFAULT 'COMPANY' NOT NULL,
  head_office_indicator      CHAR(1),
  organisation_description   VARCHAR2(255),
  primary_contact_full_name  VARCHAR2(70),
  organisational_unit_type   VARCHAR2(20),
  CONSTRAINT pk_organisation PRIMARY KEY (party_pk),
  CONSTRAINT ck_orga_head_office_indicator CHECK (head_office_indicator IN ('Y', 'N', '?'))
);
CREATE INDEX ix_orga_func_pri_contact ON organisation (primary_contact_full_name, party_pk);

CREATE TABLE party_contact_address (
  party_contact_address_pk  NUMBER NOT NULL,
  party_pk                  NUMBER,
  party_role_pk             NUMBER,
  telecom_address_pk        NUMBER,
  address_pk                NUMBER,
  party_contact_from_date   DATE   DEFAULT DATE '2000-01-01' NOT NULL,
  party_contact_to_date     DATE,
  CONSTRAINT pk_party_contact_address PRIMARY KEY (party_contact_address_pk)
);
CREATE INDEX ix_pcad_address_party_contact ON party_contact_address (address_pk, party_contact_address_pk);

-- HAZARD (mirrors canonical, which also has no natural-key UK here): the surrogate
-- PK does not stop multiple open (to_date NULL) usage rows per PARTY_CONTACT_ADDRESS.
-- find-customers joins ADDRESS_USAGE on party_contact_address_pk, so a future seed
-- adding a second open usage row per PCA would fan out results. The seed keeps
-- exactly one open usage row per PCA; do not add duplicates. (Same applies to
-- PARTY_VERSION per (party_pk, party_type, to_datetime).)
CREATE TABLE address_usage (
  address_usage_pk             NUMBER       GENERATED BY DEFAULT AS IDENTITY,
  party_contact_address_pk     NUMBER       NOT NULL,
  address_usage_type           VARCHAR2(20) NOT NULL,
  address_usage_name           VARCHAR2(35),
  address_usage_language_cd    VARCHAR2(20),
  preferred_contact_method_ind CHAR(1),
  address_usage_from_date      DATE         DEFAULT DATE '2000-01-01' NOT NULL,
  address_usage_to_date        DATE,
  CONSTRAINT pk_address_usage PRIMARY KEY (address_usage_pk),
  CONSTRAINT ck_ausa_contact_method_ind CHECK (preferred_contact_method_ind IN ('Y', 'N', '?'))
);

CREATE TABLE alt_party_identity (
  alt_party_identity_pk      NUMBER       GENERATED BY DEFAULT AS IDENTITY,
  party_pk                   NUMBER       NOT NULL,
  alt_party_identity_type    VARCHAR2(20) NOT NULL,
  alt_party_identity_value   VARCHAR2(80),
  alt_party_identity_from_date DATE       DEFAULT DATE '2000-01-01' NOT NULL,
  alt_party_identity_to_date DATE,
  CONSTRAINT pk_alt_party_identity PRIMARY KEY (alt_party_identity_pk)
);
CREATE INDEX ix_apid_pk_type ON alt_party_identity (party_pk, alt_party_identity_type);
CREATE INDEX ix_apid_type_value ON alt_party_identity (alt_party_identity_type, alt_party_identity_value);

