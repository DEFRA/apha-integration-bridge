BEGIN EXECUTE IMMEDIATE 'DROP TABLE person CASCADE CONSTRAINTS PURGE';                 EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE party_version CASCADE CONSTRAINTS PURGE';          EXCEPTION WHEN OTHERS THEN NULL; END;
