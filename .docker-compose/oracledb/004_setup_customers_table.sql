-- ── Customer/party cluster — aligned to canonical SAM schema ─────────────────
-- party / party_state / party_role / bs7666_address are owned by 001 (canonical
-- there). The tables below are created here; DROPped first so re-boot over a
-- persisted volume recreates them fresh. New NOT NULL columns use literal
-- DEFAULTs; surrogate PKs use IDENTITY. FKs are added in 009 after all data loads.
-- (The old 002 "ALTER party ADD secondary_contact_full_name" is gone — 001's
--  canonical PARTY already has that column.)
BEGIN EXECUTE IMMEDIATE 'DROP TABLE address_usage CASCADE CONSTRAINTS PURGE';          EXCEPTION WHEN OTHERS THEN NULL; END;
/
BEGIN EXECUTE IMMEDIATE 'DROP TABLE party_contact_address CASCADE CONSTRAINTS PURGE';  EXCEPTION WHEN OTHERS THEN NULL; END;
